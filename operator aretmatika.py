# Mendefinisikan dua variabel
a = 10
b = 3

# Operator penjumlahan (+)
hasil_tambah = a + b
print("Hasil penjumlahan:", hasil_tambah)

# Operator pengurangan (-)
hasil_kurang = a - b
print("Hasil pengurangan:", hasil_kurang)

# Operator perkalian (*)
hasil_kali = a * b
print("Hasil perkalian:", hasil_kali)

# Operator pembagian (/)
hasil_bagi = a / b
print("Hasil pembagian:", hasil_bagi)

# Operator pembagian bulat (//)
hasil_bagi_bulat = a // b
print("Hasil pembagian bulat:", hasil_bagi_bulat)

# Operator sisa bagi atau modulus (%)
hasil_modulus = a % b
print("Hasil sisa bagi:", hasil_modulus)

# Operator pemangkatan (**)
hasil_pangkat = a ** b
print("Hasil pemangkatan:", hasil_pangkat)
