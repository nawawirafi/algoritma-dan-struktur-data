#Cara Penggunaan Tipe Data Number Python
a = 20
b = 50.75
c = 2j
print(a)
print(b)
print(c)

#Untuk memeriksa tipe data dari sebuah variabel, 
# terdapat function type() bawaan Python. Berikut
#  contoh penggunaannya:

a = 20
b = 50.75
c = 2j
print(type(a))
print(type(b))
print(type(c))

#Nilai Maksimum untuk Tipe Data Number
d = 999
e = -99999
f = 99999999999999999999
 
print(type(d))
print(type(e))
print(type(f))

#Penulisan Notasi Ilmiah (Scientific Notation)
#Untuk tipe data float, kita bisa menulisnya menggunakan 
#notasi ilmiah atau dalam bahasa inggris disebut sebagai
#scientific notation.

g = 4e2
h = 4e-2
i = 1.25e2
print(g)
print(h)
print(i)