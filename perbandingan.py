# Mendefinisikan dua variabel
x = 10
y = 15

# Operator perbandingan
# Lebih besar (>)
print("Apakah x lebih besar dari y?", x > y)

# Lebih kecil (<)
print("Apakah x lebih kecil dari y?", x < y)

# Sama dengan (==)
print("Apakah x sama dengan y?", x == y)

# Tidak sama dengan (!=)
print("Apakah x tidak sama dengan y?", x != y)

# Lebih besar atau sama dengan (>=)
print("Apakah x lebih besar atau sama dengan y?", x >= y)

# Lebih kecil atau sama dengan (<=)
print("Apakah x lebih kecil atau sama dengan y?", x <= y)
